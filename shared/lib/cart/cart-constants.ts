/**
 * Максимальное количество товара одной позиции в корзине.
 * Best practice: ограничение предотвращает случайные ошибки и злоупотребления.
 * Обычно устанавливается в диапазоне 50-99 для розничной торговли.
 */
export const MAX_CART_ITEM_QUANTITY = 99;

/**
 * Максимальное количество различных позиций в корзине.
 */
export const MAX_CART_ITEMS_COUNT = 50;

/**
 * Срок жизни cookie корзины в секундах (30 дней).
 * Best practice: достаточно долгий срок для сохранения корзины гостя,
 * но не слишком долгий для безопасности.
 */
export const CART_COOKIE_MAX_AGE = 60 * 60 * 24 * 30; // 30 дней
